# Looping Through a int

#This is for the loop will print numbers from 1 to 100
for i in range(1, 101):        # range() function
    print(i)


# Looping Through a List
list_of_numbers = [1, 2, 3, 4, 5]
for number in list_of_numbers:
   print(number)

# looping through a string
for number in list_of_numbers:
    print(number)

for char in "Hello":
   print(char)

for character in "python":
   print(character)

# looping through a list
names = ["samarth", "omkaer", "prabhu", "ramesh"]
for name in names:
    print(name)

fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)

# looping through a dictionary
my_dict = {'a':1,'b':2,'c':3}
for key, value in my_dict.items():
   print(f"key: {key}, value: {value}")

# looping through a Sets
my_set = {1,2,3,4,5}
for key, value in my_set:
   print(my_set)

